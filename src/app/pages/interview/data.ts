function addCommonHeader(common,arr){
  for(let i = 0;i<common.length;i++){
    arr.splice(i,0,common[i]);
  }
  return arr;
}
function addCommonTable(common,arr){
  for(let i = 0;i<common.length;i++){
    addCommonHeader(common[i],arr[i]);
  }
  return arr;
}
function initTable(row,col,ele){
  let arr = [];
  for (let i = 0;i<row;i++){
    let temp = [];
    for (let j =0;j<col;j++){
      temp.push(ele);
    }
    arr.push(temp);
  }
  return arr;
}
